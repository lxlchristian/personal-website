// Particles

(function initHeroParticles() {
  const canvas = document.getElementById("heroParticles");
  const hero = document.querySelector(".hero");

  if (!canvas || !hero) return;
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  const ctx = canvas.getContext("2d");
  let particles = [];
  let animationId = null;
  let mouse = {
    x: null,
    y: null,
    active: false
  };

  function getHeroSize() {
    return {
      width: hero.clientWidth,
      height: hero.clientHeight
    };
  }

  function resizeCanvas() {
    const { width, height } = getHeroSize();
    const dpr = Math.min(window.devicePixelRatio || 1, 2);

    canvas.width = width * dpr;
    canvas.height = height * dpr;
    canvas.style.width = width + "px";
    canvas.style.height = height + "px";

    ctx.setTransform(dpr, 0, 0, dpr, 0, 0);

    createParticles(width, height);
  }

  function createParticles(width, height) {
    const count = Math.max(36, Math.min(72, Math.floor(width / 22)));

    particles = Array.from({ length: count }, () => {
      const isGold = Math.random() > 0.28;

      return {
        x: Math.random() * width,
        y: Math.random() * height,
        vx: (Math.random() - 0.5) * 0.28,
        vy: (Math.random() - 0.5) * 0.28,
        size: Math.random() * 1.8 + 0.8,
        alpha: Math.random() * 0.45 + 0.2,
        color: isGold
          ? "212, 169, 81"
          : "79, 152, 163"
      };
    });
  }

  function drawGlow(x, y, size, color, alpha) {
    const glow = ctx.createRadialGradient(x, y, 0, x, y, size * 6);
    glow.addColorStop(0, `rgba(${color}, ${alpha})`);
    glow.addColorStop(0.4, `rgba(${color}, ${alpha * 0.28})`);
    glow.addColorStop(1, `rgba(${color}, 0)`);

    ctx.beginPath();
    ctx.fillStyle = glow;
    ctx.arc(x, y, size * 6, 0, Math.PI * 2);
    ctx.fill();
  }

  function drawParticle(p) {
    drawGlow(p.x, p.y, p.size, p.color, p.alpha * 0.35);

    ctx.beginPath();
    ctx.fillStyle = `rgba(${p.color}, ${p.alpha})`;
    ctx.arc(p.x, p.y, p.size, 0, Math.PI * 2);
    ctx.fill();
  }

  function drawConnections() {
    for (let i = 0; i < particles.length; i++) {
      for (let j = i + 1; j < particles.length; j++) {
        const a = particles[i];
        const b = particles[j];
        const dx = a.x - b.x;
        const dy = a.y - b.y;
        const dist = Math.hypot(dx, dy);

        if (dist < 95) {
          const opacity = (1 - dist / 95) * 0.08;
          ctx.beginPath();
          ctx.strokeStyle = `rgba(220, 200, 160, ${opacity})`;
          ctx.lineWidth = 0.8;
          ctx.moveTo(a.x, a.y);
          ctx.lineTo(b.x, b.y);
          ctx.stroke();
        }
      }
    }
  }

  function updateParticles(width, height) {
    for (const p of particles) {
      p.x += p.vx;
      p.y += p.vy;

      if (p.x < -20) p.x = width + 20;
      if (p.x > width + 20) p.x = -20;
      if (p.y < -20) p.y = height + 20;
      if (p.y > height + 20) p.y = -20;

      if (mouse.active && mouse.x !== null && mouse.y !== null) {
        const dx = p.x - mouse.x;
        const dy = p.y - mouse.y;
        const dist = Math.hypot(dx, dy) || 1;
        const radius = 140;

        if (dist < radius) {
          const force = (radius - dist) / radius;
          p.x += (dx / dist) * force * 0.9;
          p.y += (dy / dist) * force * 0.9;
        }
      }

      drawParticle(p);
    }
  }

  function animate() {
    const { width, height } = getHeroSize();
    ctx.clearRect(0, 0, width, height);

    updateParticles(width, height);
    drawConnections();

    animationId = requestAnimationFrame(animate);
  }

  function handleMouseMove(event) {
    const rect = hero.getBoundingClientRect();
    mouse.x = event.clientX - rect.left;
    mouse.y = event.clientY - rect.top;
    mouse.active = true;
  }

  function handleMouseLeave() {
    mouse.x = null;
    mouse.y = null;
    mouse.active = false;
  }

  hero.addEventListener("mousemove", handleMouseMove);
  hero.addEventListener("mouseleave", handleMouseLeave);
  window.addEventListener("resize", resizeCanvas);

  resizeCanvas();
  animate();
})();