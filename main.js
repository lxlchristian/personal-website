/* ── Scroll reveal ───────────────────────────────────────── */
function initReveal() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      const delay = entry.target.closest('.track-list')
        ? Array.from(entry.target.parentElement.children).indexOf(entry.target) * 60
        : 0;
      setTimeout(() => entry.target.classList.add('visible'), delay);
      observer.unobserve(entry.target);
    });
  }, { threshold: 0.12, rootMargin: '0px 0px -48px 0px' });
  document.querySelectorAll('.reveal, .fade-in').forEach(el => observer.observe(el));
  return observer;
}
const revealObserver = initReveal();

